task usercontrol()
{
	// User control code here, inside the loop
	clearLCDLine(0);
	clearLCDLine(1);
	stopTask(LCDControl);
	int y;
	int x;
	int threshhigh = 100;
	int threshlow = 20;

	while (true)
	{
		motor[Roller] = 0;
		motor[LeftLift] = 0;
		motor[RightLift] = 0;
		// make joystick move far enough before using values
		if(abs(vexRT[Ch3]) >= threshhigh)
		{
			y = vexRT[Ch3] * .8;
		}
		else if(abs(vexRT[Ch3]) <= threshlow)
		{
			y = 0;
		}
		else
		{
			y = vexRT[Ch3];
		}
		// make joystick move far enough before using values
		if(abs(vexRT[Ch1]) >= threshhigh)
		{
			x = vexRT[Ch1] * .8;
		}
		else if(abs(vexRT[Ch1]) <= threshlow)
		{
			x = 0;
		}

		else
		{
			x = vexRT[Ch1];
		}


		if(vexRT[Btn5U] == 1)
		{
			/*
			motor[LeftFrontDrive] = x/3 + y/2.5;
			motor[RightFrontDrive] = -x/3 + y/2.5;
			motor[LeftRearDrive] = x/3 + y/2.5;
			motor[RightRearDrive] = -x/3 + y/2.5;
			*/
			motor[LeftFrontDrive] = x + y;
			motor[RightFrontDrive] = -x + y;
			motor[LeftRearDrive] = x + y;
			motor[RightRearDrive] = -x + y;
		}
		else
		{
			/*
			motor[LeftFrontDrive] = x + y;
			motor[RightFrontDrive] = -x + y;
			motor[LeftRearDrive] = x + y;
			motor[RightRearDrive] = -x + y;
			*/
			motor[LeftFrontDrive] = x / 1.5 + y / 1.5;
			motor[RightFrontDrive] = -x / 1.5 + y / 1.5;
			motor[LeftRearDrive] = x / 1.5 + y / 1.5;
			motor[RightRearDrive] = -x / 1.5 + y / 1.5;
		}
		/*
		speed = 2600 - SensorValue[SwingPot];
		motor[LeftLift] = speed * 0.05;
		motor[RightLift] = speed * 0.05;
		motor[Roller] = -40;
		*/


		if(vexRT[Btn8U] == 1)
		{
			stopTask(mogoUp);
			startTask(mogoDown);
		}
		else if(vexRT[Btn8D] == 1)
		{
			stopTask(mogoDown);
			startTask(mogoUp);
		}

		if(vexRT[Btn6U] == 1)
		{
			autoStack();
		}
		if(vexRT[Btn7R] == 1)
		{
			driveReverseP(400);
		}
		if(vexRT[Btn7L] == 1)
		{
			turnLeft(500);
			wait1Msec(1000);
			turnRight(500);
			wait1Msec(1000);
			turnLeft(1050);
			wait1Msec(1000);
			turnRight(1050);
			wait1Msec(1000);

		}
		if(vexRT[Btn7D] == 1)
		{
			drivePID(200);
			wait1Msec(100);
			drivePID(400);
			wait1Msec(100);
			drivePID(400);
		}
		wait1Msec(20);
	}
	if(vexRT[Btn7U] == 1)
	{
		auton = 0;
		startTask(autonomous);
		stopTask(usercontrol);
	}
}
